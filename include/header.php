            if ($parent.hasClass('mobile-menu__item--open')) {
                $parent.removeClass('mobile-menu__item--open');
                $submenu.css('max-height', 0);
                $icon.css('transform', 'rotate(0deg)');
            } else {
                $parent.addClass('mobile-menu__item--open');
                const submenuHeight = $submenu[0].scrollHeight;
                $submenu.css('max-height', submenuHeight + 'px');
                $icon.css('transform', 'rotate(180deg)');
            }
        });

        // Toggle nested submenus (second level)
        $(document).on('click', '.mobile-menu__link--nested .mobile-menu__toggle', function(e) {
            e.preventDefault();
            e.stopPropagation();

            const $parent = $(this).closest('.mobile-menu__item--nested');
            const $submenu = $parent.find('> .mobile-menu__submenu--nested');
            const $icon = $(this).find('i');

            // Toggle nested submenu
            if ($parent.hasClass('mobile-menu__item--open')) {
                $parent.removeClass('mobile-menu__item--open');
                $submenu.css('max-height', 0);
                $icon.css('transform', 'rotate(0deg)');
            } else {
                $parent.addClass('mobile-menu__item--open');
                const submenuHeight = $submenu[0].scrollHeight;
                $submenu.css('max-height', submenuHeight + 'px');
                $icon.css('transform', 'rotate(180deg)');
            }
        });

        // Handle menu link clicks
        $('.mobile-menu__link').on('click', function(e) {
            // If it's a toggle link, don't navigate
            if ($(this).find('.mobile-menu__toggle').length > 0) {
                return;
            }

            // Close sidebar on mobile after clicking a link
            closeOffcanvas();
        });

        // Student Zone modal in mobile sidebar
        $('.mobile-menu__btn.open-admission-modal').on('click', function() {
            closeOffcanvas();
            setTimeout(() => {
                document.getElementById('admissionModal').classList.add('active');
            }, 300);
        });

        // Close button
        $(".offcanvas__close button").on("click", function(e) {
            e.stopPropagation();
            closeOffcanvas();
        });

        // Overlay click
        $(".offcanvas__overlay").on("click", function() {
            closeOffcanvas();
        });

        // Open sidebar (triggered by hamburger button in your header)
        $(".sidebar__toggle").on("click", function(e) {
            e.preventDefault();
            e.stopPropagation();
            openOffcanvas();
        });

        // Escape key to close
        $(document).on("keydown", function(e) {
            if (e.key === "Escape" && $(".offcanvas__info").hasClass("info-open")) {
                closeOffcanvas();
            }
        });

        // Close offcanvas when clicking outside on mobile
        $(document).on('click touchstart', function(e) {
            if ($(window).width() <= 991 &&
                $(".offcanvas__info").hasClass("info-open") &&
                !$(e.target).closest('.offcanvas__info').length &&
                !$(e.target).closest('.sidebar__toggle').length) {
                closeOffcanvas();
            }
        });

        // Functions
        function openOffcanvas() {
            $(".offcanvas__info").addClass("info-open");
            $(".offcanvas__overlay").addClass("overlay-open");
            $("body").addClass("offcanvas-open");
            $('html').css('overflow', 'hidden');
        }

        function closeOffcanvas() {
            $(".offcanvas__info").removeClass("info-open");
            $(".offcanvas__overlay").removeClass("overlay-open");
            $("body").removeClass("offcanvas-open");
            $('html').css('overflow', '');

            // Close all submenus
            $('.mobile-menu__item--has-children').removeClass('mobile-menu__item--open');
            $('.mobile-menu__submenu').css('max-height', 0);
            $('.mobile-menu__toggle i').css('transform', 'rotate(0deg)');
        }

        // Handle window resize - close sidebar if window becomes larger
        $(window).on('resize', function() {
            if ($(window).width() > 991 && $(".offcanvas__info").hasClass("info-open")) {
                closeOffcanvas();
            }
        });
    });
</script>

